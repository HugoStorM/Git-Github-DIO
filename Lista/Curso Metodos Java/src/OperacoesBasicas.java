
public class OperacoesBasicas {	
	
	public static void soma (double valor1, double valor2) {
		
	 double resultado = valor1+valor2;
	 System.out.println("A soma de "+ valor1 + " e " + valor2 + " é igual a: " + resultado);
	 
	}
	
	public static void subtracao(double valor1, double valor2) {
		
		double resultado = valor1 - valor2;
		System.out.println("A subtração de "+ valor1 + " e " + valor2 + " é igual a: " + resultado);
	}
	
	public static void multipicacao (double valor1, double valor2) {
		
		double resultado = valor1 * valor2;
		System.out.println("A multiplição de "+ valor1 + " e " + valor2 + " é igual a: " + resultado);
	}
	
	public static void divisao (double valor1, double valor2) {
		
		double resultado = valor1 / valor2;
		System.out.println("A divisao de "+ valor1 + " e " + valor2 + " é igual a: " + resultado);
	}
	

}
